// Q 1. Write a simple C++ program to display "Hello, World!". 
// Objective: Understand the basic structure of a C++ program, including #include, main(), and cout. 

#include<iostream>
using namespace std;

int main(){

    cout << "Hello world";

    return 0;
}

// Explanation: 
// #include <iostream> is used to include the input-output library. 
// using namespace std; allows us to avoid writing std::cout. 
// int main() is the starting point of the program. 
// cout is used to display output. 
// return 0; ends the program. 